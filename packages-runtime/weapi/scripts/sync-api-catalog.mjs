import fs from 'node:fs/promises'
import { createRequire } from 'node:module'
import path from 'node:path'
import process from 'node:process'
import ts from 'typescript'

const ROOT_DIR = path.resolve(import.meta.dirname, '..')
const OUTPUT_ENTRY_FILE = path.join(ROOT_DIR, 'src/core/apiCatalog.ts')
const OUTPUT_DIR = path.join(ROOT_DIR, 'src/core/apiCatalog')
const METHOD_CHUNK_SIZE = 180
const VIRTUAL_ENTRY = '/virtual/weapi-api-catalog-entry.ts'
const require = createRequire(import.meta.url)

const NON_ALPHANUMERIC_RE = /[^a-z0-9]/gi
const CAMEL_CASE_BOUNDARY_RE = /([a-z0-9])([A-Z])/g
const NON_ALPHANUMERIC_SEPARATOR_RE = /[^a-z0-9]+/gi
const LEADING_TRAILING_UNDERSCORES_RE = /^_+|_+$/g
const CRLF_RE = /\r\n/g

const TYPE_REFERENCE_SOURCE = `
declare const __wx: WechatMiniprogram.Wx

declare const __my: typeof my

declare const __tt: typeof tt
`

function parseArgs() {
  return {
    check: process.argv.includes('--check'),
  }
}

function uniqueSorted(names) {
  return [...new Set(names)].sort((a, b) => a.localeCompare(b))
}

function formatLiteralList(values) {
  return values.map(item => `  '${item}',`).join('\n')
}

function chunkArray(values, size) {
  const chunks = []
  for (let index = 0; index < values.length; index += size) {
    chunks.push(values.slice(index, index + size))
  }
  return chunks
}

function toPascalCaseIdentifier(value) {
  const sanitized = String(value).replace(NON_ALPHANUMERIC_RE, '')
  if (!sanitized) {
    return 'Unknown'
  }
  return `${sanitized[0].toUpperCase()}${sanitized.slice(1)}`
}

function getChunkFileSuffix(chunkValues) {
  if (chunkValues.length === 0) {
    return 'UnknownToUnknown'
  }
  const first = toPascalCaseIdentifier(chunkValues[0])
  const last = toPascalCaseIdentifier(chunkValues.at(-1))
  return `${first}To${last}`
}

function toUpperSnakeIdentifier(value) {
  const normalized = String(value)
    .replace(CAMEL_CASE_BOUNDARY_RE, '$1_$2')
    .replace(NON_ALPHANUMERIC_SEPARATOR_RE, '_')
    .replace(LEADING_TRAILING_UNDERSCORES_RE, '')
    .toUpperCase()
  return normalized || 'UNKNOWN'
}

function getChunkConstSuffix(chunkValues) {
  if (chunkValues.length === 0) {
    return 'UNKNOWN_TO_UNKNOWN'
  }
  const first = toUpperSnakeIdentifier(chunkValues[0])
  const last = toUpperSnakeIdentifier(chunkValues.at(-1))
  return `${first}_TO_${last}`
}

function formatMethodModule({
  exportName,
  values,
}) {
  const chunks = chunkArray(values, METHOD_CHUNK_SIZE)
  if (chunks.length === 1) {
    return `/**
 * @description 三端 API 名单（由 typings 自动提取，请勿手工修改）
 * @generated by scripts/sync-api-catalog.mjs
 */
export const ${exportName} = [
${formatLiteralList(values)}
] as const
`
  }
  const partConstNames = chunks.map(chunkValues => `${exportName}_${getChunkConstSuffix(chunkValues)}`)
  const partSources = chunks.map((chunkValues, index) => `const ${partConstNames[index]} = [
${formatLiteralList(chunkValues)}
] as const`).join('\n\n')
  const mergedLines = partConstNames.map(partName => `  ...${partName},`).join('\n')
  return `/**
 * @description 三端 API 名单（由 typings 自动提取，请勿手工修改）
 * @generated by scripts/sync-api-catalog.mjs
 */
${partSources}

export const ${exportName} = [
${mergedLines}
] as const
`
}

function generateMethodModuleFiles({
  filePrefix,
  exportName,
  values,
}) {
  const files = new Map()
  const chunks = chunkArray(values, METHOD_CHUNK_SIZE)
  const mainFilePath = path.join(OUTPUT_DIR, `${filePrefix}.ts`)
  if (chunks.length === 1) {
    files.set(mainFilePath, formatMethodModule({
      exportName,
      values,
    }))
    return files
  }
  const partImports = []
  const mergeLines = []

  for (let index = 0; index < chunks.length; index += 1) {
    const partFileSuffix = getChunkFileSuffix(chunks[index])
    const partConstName = `${exportName}_${getChunkConstSuffix(chunks[index])}`
    const partFileName = `${filePrefix}${partFileSuffix}.ts`
    const partFilePath = path.join(OUTPUT_DIR, partFileName)
    const partFile = `/**
 * @description 三端 API 名单（由 typings 自动提取，请勿手工修改）
 * @generated by scripts/sync-api-catalog.mjs
 */
export const ${partConstName} = [
${formatLiteralList(chunks[index])}
] as const
`
    files.set(partFilePath, partFile)
    partImports.push(`import { ${partConstName} } from './${filePrefix}${partFileSuffix}'`)
    mergeLines.push(`  ...${partConstName},`)
  }

  const mainFile = `/**
 * @description 三端 API 名单（由 typings 自动提取，请勿手工修改）
 * @generated by scripts/sync-api-catalog.mjs
 */
${partImports.join('\n')}

export const ${exportName} = [
${mergeLines.join('\n')}
] as const
`
  files.set(mainFilePath, mainFile)
  return files
}

function normalizeLineEndings(text) {
  return text.replace(CRLF_RE, '\n')
}

function isTextEquivalent(a, b) {
  return normalizeLineEndings(a) === normalizeLineEndings(b)
}

function getTypePackageVersion(packageName) {
  const packageJsonPath = require.resolve(`${packageName}/package.json`, { paths: [ROOT_DIR] })
  const packageJson = require(packageJsonPath)
  return String(packageJson.version)
}

function createProgram() {
  const wxDtsPath = require.resolve('miniprogram-api-typings/types/wx/index.d.ts', { paths: [ROOT_DIR] })
  const myDtsPath = require.resolve('@mini-types/alipay/types/index.d.ts', { paths: [ROOT_DIR] })
  const ttDtsPath = require.resolve('@douyin-microapp/typings/index.d.ts', { paths: [ROOT_DIR] })

  const compilerOptions = {
    target: ts.ScriptTarget.ES2022,
    module: ts.ModuleKind.ESNext,
    moduleResolution: ts.ModuleResolutionKind.Bundler,
    strict: true,
    skipLibCheck: true,
    lib: ['lib.es2022.d.ts'],
    types: ['miniprogram-api-typings', '@mini-types/alipay', '@douyin-microapp/typings'],
  }

  const host = ts.createCompilerHost(compilerOptions)

  host.getSourceFile = (fileName, languageVersion) => {
    if (fileName === VIRTUAL_ENTRY) {
      return ts.createSourceFile(fileName, TYPE_REFERENCE_SOURCE, languageVersion, true, ts.ScriptKind.TS)
    }
    const text = ts.sys.readFile(fileName)
    if (text == null) {
      return undefined
    }
    return ts.createSourceFile(fileName, text, languageVersion, true)
  }

  host.readFile = (fileName) => {
    if (fileName === VIRTUAL_ENTRY) {
      return TYPE_REFERENCE_SOURCE
    }
    return ts.sys.readFile(fileName)
  }

  host.fileExists = (fileName) => {
    if (fileName === VIRTUAL_ENTRY) {
      return true
    }
    return ts.sys.fileExists(fileName)
  }

  return ts.createProgram([VIRTUAL_ENTRY, wxDtsPath, myDtsPath, ttDtsPath], compilerOptions, host)
}

function getVariableType(program, checker, variableName) {
  const sourceFile = program.getSourceFile(VIRTUAL_ENTRY)
  if (!sourceFile) {
    throw new Error('无法读取虚拟类型入口文件')
  }

  let resolvedSymbol

  ts.forEachChild(sourceFile, (node) => {
    if (!ts.isVariableStatement(node)) {
      return
    }
    for (const declaration of node.declarationList.declarations) {
      if (!ts.isIdentifier(declaration.name)) {
        continue
      }
      if (declaration.name.text !== variableName) {
        continue
      }
      resolvedSymbol = checker.getSymbolAtLocation(declaration.name)
    }
  })

  if (!resolvedSymbol || !resolvedSymbol.valueDeclaration) {
    throw new Error(`无法解析变量类型：${variableName}`)
  }

  return checker.getTypeOfSymbolAtLocation(resolvedSymbol, resolvedSymbol.valueDeclaration)
}

function collectApiMembers(checker, containerType) {
  const methods = []
  const nonFunctionMembers = []

  for (const property of checker.getPropertiesOfType(containerType)) {
    const name = property.getName()
    if (name.startsWith('__@')) {
      continue
    }

    const declaration = property.valueDeclaration ?? property.declarations?.[0]
    if (!declaration) {
      continue
    }

    const propertyType = checker.getTypeOfSymbolAtLocation(property, declaration)
    const callSignatures = checker.getSignaturesOfType(propertyType, ts.SignatureKind.Call)

    if (callSignatures.length > 0) {
      methods.push(name)
      continue
    }

    nonFunctionMembers.push(name)
  }

  return {
    methods: uniqueSorted(methods),
    nonFunctionMembers: uniqueSorted(nonFunctionMembers),
  }
}

