function element(
  name: string,
  compatibility: ApiCompatibility,
  description: string,
  anchor: string,
  page = 'built-in-special-elements',
): WevuApiSeed {
  return {
    name,
    description,
    href: `${templateDocs}#${anchor}`,
    vueHref: `${vueApi}/${page}.html#${name.replace(/[<>]/g, '').toLowerCase()}`,
    group: '特殊元素与内置组件',
    kind: 'element',
    phase: 'compile',
    compatibility,
    scopes: ['page', 'component'],
    keywords: ['模板元素', '内置组件'],
  }
}

