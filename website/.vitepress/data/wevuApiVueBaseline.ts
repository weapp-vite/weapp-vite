import type { WevuApiSeed } from './wevuApiCatalogTypes'

const vueApi = 'https://cn.vuejs.org/api'

function unsupported(
  name: string,
  group: string,
  kind: WevuApiSeed['kind'],
  page: string,
  anchor: string,
  keywords: string[] = [],
): WevuApiSeed {
  const vueHref = `${vueApi}/${page}.html#${anchor}`
  return {
    name,
    description: 'Vue 提供该能力，Wevu 当前没有对应实现。',
    href: vueHref,
    vueHref,
    group,
    kind,
    phase: kind === 'type' ? 'type' : 'runtime',
    compatibility: 'unsupported',
    keywords: ['Vue API', '不支持', ...keywords],
  }
}

